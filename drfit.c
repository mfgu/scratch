/* Subroutine */ int drfit_(integer *iz, integer *in, doublereal *t, 
	doublereal *r__)
{
    /* Initialized data */

    static integer ind[28] = { -1 };

    /* Builtin functions */
    double exp(doublereal), pow_dd(doublereal *, doublereal *);

    /* Local variables */
    static integer i__;

/* ***************************************************** */
/* DR rate coefficients using the fitting formulae and */
/* data from Mazzotta et al. A&AS, 133, 403. */
/* Input: */
/*     iz, Nuclear charge */
/*     in, number of electrons of the recombining ion, 0 to iz-1 */
/*     t,  temperature in eV. */
/*     r,  result in cm3s-1 */
/* ****************************************************** */
    *r__ = 0.f;
    if (*iz < 2 || *iz > 28) {
	return 0;
    }
    if (ind[0] < 0) {
	ind[0] = 0;
	ind[1] = 1;
	for (i__ = 2; i__ <= 27; ++i__) {
	    ind[i__] = ind[i__ - 1] + i__ - 1;
	}
    }
    i__ = ind[*iz - 1] + *iz - *in - 1;
    if (dr_1.drc[(i__ << 2) - 4] > 0.f) {
	*r__ += dr_1.drc[(i__ << 2) - 4] * exp(-dr_1.dre[(i__ << 2) - 4] / *t)
		;
    }
    if (dr_1.drc[(i__ << 2) - 3] > 0.f) {
	*r__ += dr_1.drc[(i__ << 2) - 3] * exp(-dr_1.dre[(i__ << 2) - 3] / *t)
		;
    }
    if (dr_1.drc[(i__ << 2) - 2] > 0.f) {
	*r__ += dr_1.drc[(i__ << 2) - 2] * exp(-dr_1.dre[(i__ << 2) - 2] / *t)
		;
    }
    if (dr_1.drc[(i__ << 2) - 1] > 0.f) {
	*r__ += dr_1.drc[(i__ << 2) - 1] * exp(-dr_1.dre[(i__ << 2) - 1] / *t)
		;
    }
    *r__ /= pow_dd(t, &c_b2);
    return 0;
} /* drfit_ */

